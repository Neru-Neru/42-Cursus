#include "libft.h"

int	ft_atoi(const char *str)
{
	int	ans;

	ans = 0;
	while (*str != '\0')
	{
		if (*str < 48 || 57 < *str)
			break ;
		ans += *str + '0';
		str++;
	}
	return (ans);
}
